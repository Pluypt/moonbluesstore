// Function to search mock data
export function searchMockData(keyword: string, limit: number = 20): Sneaker[] {
    if (!keyword || keyword.trim() === '') {
        return MOCK_SNEAKERS.slice(0, limit);
    }

    const searchTerm = keyword.toLowerCase().trim();
    
    const filtered = MOCK_SNEAKERS.filter(sneaker => 
        sneaker.shoeName.toLowerCase().includes(searchTerm) ||
        sneaker.brand.toLowerCase().includes(searchTerm) ||
        sneaker.silhoutte.toLowerCase().includes(searchTerm) ||
        sneaker.styleID.toLowerCase().includes(searchTerm)
    );

    return filtered.slice(0, limit);
}
